    private void btnPesquisarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPesquisarMouseClicked
        // TODO add your handling code here:
        Connection con = Conexao.abrirConexao();
         PessoaDAO cd = new PessoaDAO(con);
         List<PessoaBean> listaPessoa = new ArrayList<PessoaBean>();
         listaPessoa = cd.listarTodos(); 
         DefaultTableModel tbm = (DefaultTableModel)tblConsulta.getModel(); 
         
         for(int i = tbm.getRowCount()-1; i >= 0; i--){ 
            tbm.removeRow(i); 
        } 
         
         int i = 0;
         
         for(PessoaBean cb : listaPessoa){ 
            tbm.addRow(new String[1]); 
            tblConsulta.setValueAt(cb.getCpf(), i, 0); 
            tblConsulta.setValueAt(cb.getNome(), i, 1); 
            tblConsulta.setValueAt(cb.getSobrenome(), i, 2);
            tblConsulta.setValueAt(cb.getIdade(), i, 3); 
            tblConsulta.setValueAt(cb.getEmail(), i, 4); 
            tblConsulta.setValueAt(cb.getRg(), i, 5); 
            tblConsulta.setValueAt(cb.getEndereco(), i, 6); 
            tblConsulta.setValueAt(cb.getComplemento(), i, 7); 
            tblConsulta.setValueAt(cb.getCep(), i, 8);
            tblConsulta.setValueAt(cb.getTelefone(), i, 9); 
            tblConsulta.setValueAt(cb.getSexo(), i, 10); 
            tblConsulta.setValueAt(cb.getNacionalidade(), i, 11); 
            i++;
         }
         Conexao.fecharConexao(con);
    }//GEN-LAST:event_btnPesquisarMouseClicked

